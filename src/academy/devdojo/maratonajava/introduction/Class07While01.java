package academy.devdojo.maratonajava.introduction;

public class Class07While01 {
    public static void main(String[] args) {
        //while, do while, for
        int c = 0;
        while (c < 10){
            System.out.println(c);
            c++;
        }

        do {
            System.out.println(c);
            c++;
        }while(c < 10);



    }
}
